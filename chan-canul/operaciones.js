function promedio(a, b, c, d, e) {
    return (parseFloat(a) + parseFloat(b) + parseFloat(c) + parseFloat(d) + parseFloat(e))* 0.3 / 50 * 100
}

function promedio2(a, b, c, d, e, f) {
    return (parseFloat(a) + parseFloat(b) + parseFloat(c) + parseFloat(d) + parseFloat(e) + parseFloat(f))* 0.3 / 60 * 100
}

function promedioexamen(a) {
    return (parseFloat(a) * 0.04) * 100
}